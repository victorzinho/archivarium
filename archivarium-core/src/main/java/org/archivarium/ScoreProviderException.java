package org.archivarium;

public class ScoreProviderException extends Exception {
	public ScoreProviderException() {
		super();
	}

	public ScoreProviderException(String message, Throwable cause) {
		super(message, cause);
	}

	public ScoreProviderException(String message) {
		super(message);
	}

	public ScoreProviderException(Throwable cause) {
		super(cause);
	}
}
